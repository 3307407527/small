function Game() {
    var _this = this;
    this.objEnemy=[
        {x:900,y:430},
        {x:1600,y:30},
        {x:100,y:730},
        {x:1600,y:730}
    ];
    this.enemys=[];
    this.nobita = new Nobita();
    this.brick = new Brick();

    for(var i=0;i<this.objEnemy.length;i++){
        this.enemys.push(new Enemy(this.objEnemy[i].x,this.objEnemy[i].y))
    }

    this.maps = map;
    this.tr = cvs.height / this.maps.length;
    this.frameX=0;
    this.frameY=0;
    this.count=0;
    this.winCount =0;

    this.update = function () {
        _this.count++;
        ctx.clearRect(0, 0, cvs.width, cvs.height);
        _this.getMap();
        _this.nobita.drawNew();

        _this.enemys.forEach(function (item) {
           item.draw();
           if(item.x>_this.nobita.x){
               item.frameY=1;
               item.frameX = ++item.frameX >= 4 ? 0 : item.frameX;
               item.x-=item.v
           }else if(item.y>_this.nobita.y){
               item.frameY=3;
               item.frameX = ++item.frameX >= 4 ? 0 : item.frameX;
               item.y-=item.v
           }
           if(item.x<_this.nobita.x){
               item.frameY=2;
               item.frameX = ++item.frameX >= 4 ? 0 : item.frameX;
               item.x+=item.v
           }else if(item.y<_this.nobita.y){
               item.frameY=0;
               item.frameX = ++item.frameX >= 4 ? 0 : item.frameX;
               item.y+=item.v
           }


           if(Math.abs(item.x-_this.nobita.x)<=20 && Math.abs(item.y-_this.nobita.y)<=20){
               self.location="bigTwo.html";
           }

        });

        if(_this.count>=1000){
            for(var i=1;i<_this.maps.length-1;i++){
                if(_this.maps[i].indexOf("2")==-1){
                    _this.winCount++;
                }
            }
        }
        if(_this.winCount == 25){
            alert("你胜利了")
        }

        window.requestAnimationFrame(_this.update);
    };

    this.getMap = function () {
        var nobitaX=Math.floor((_this.nobita.x )/ (cvs.width / _this.maps[0].length))+1;
        var nobitaY=Math.floor(_this.nobita.y / (cvs.height / _this.maps.length))+1;
        var nobitaWidth=Math.floor((_this.nobita.x+60 )/ (cvs.width / _this.maps[0].length));
        var nobitaHeight=Math.floor((_this.nobita.y+60) / (cvs.height / _this.maps.length));


        for (var i = 0; i < _this.maps.length; i++) {
            for (var j = 0; j < _this.maps[i].length; j++) {
                if (_this.maps[i][j] == 1) {
                    ctx.beginPath();
                    var img = new Image();
                    img.src = "img/lanzt.png";
                    ctx.drawImage(img, _this.tr * j, _this.tr * i);
                }else if(_this.maps[i][j]==2){
                    ctx.beginPath();
                    var imgXing=new Image();
                    imgXing.src="img/qian.png";
                    ctx.drawImage(imgXing,_this.tr * j+(_this.tr-imgXing.width)/2, _this.tr * i+(_this.tr-imgXing.height)/2);

                }

            }
        }
        if(_this.maps[nobitaY][nobitaX] == 2 ||_this.maps[nobitaHeight][nobitaWidth] == 2){
            _this.maps[nobitaY][nobitaX]=0;

        }

    };

    document.onkeydown = function (e) {
        var j = Math.floor((_this.nobita.x )/ (cvs.width / _this.maps[0].length))+1 ;//左
        var i = Math.floor(_this.nobita.y / (cvs.height / _this.maps.length))+1 ;//上
        var l = Math.floor((_this.nobita.x+60) / (cvs.width / _this.maps[0].length)) -1 ;//右
        var d =Math.floor((_this.nobita.y+60) / (cvs.height / _this.maps.length))-1 ;//下


        if (e.keyCode == 65) {
            _this.nobita.frameX = ++_this.nobita.frameX >= 4 ? 0 : _this.nobita.frameX;
            _this.nobita.frameY = 1;
            if (_this.maps[i][j - 1] != 1) {
                _this.nobita.x -= _this.nobita.v
            } else {
                _this.nobita.x = _this.nobita.x;
            }
        } else if (e.keyCode == 87) {
            _this.nobita.frameX = ++_this.nobita.frameX >= 4 ? 0 : _this.nobita.frameX;
            _this.nobita.frameY = 3;

            if (_this.maps[i - 1][j] != 1) {
                _this.nobita.y -= _this.nobita.v
            } else {
                _this.nobita.y = _this.nobita.y;
            }
        } else if (e.keyCode == 68) {
            _this.nobita.frameX = ++_this.nobita.frameX >= 4 ? 0 : _this.nobita.frameX;
            _this.nobita.frameY = 2;
            if (_this.maps[d][l + 1] != 1) {
                _this.nobita.x += _this.nobita.v
            } else {
                _this.nobita.x = _this.nobita.x;
            }
        } else if (e.keyCode == 83) {
            _this.nobita.frameX = ++_this.nobita.frameX >= 4 ? 0 : _this.nobita.frameX;
            _this.nobita.frameY = 0;
            if (_this.maps[d + 1][l] != 1) {
                _this.nobita.y += _this.nobita.v
            } else {
                _this.nobita.y = _this.nobita.y;
            }
        }
    }
}